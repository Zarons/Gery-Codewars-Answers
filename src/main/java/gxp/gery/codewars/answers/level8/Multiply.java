package gxp.gery.codewars.answers.level8;

public class Multiply {
    public static Double multiply(Double a, Double b) {
        return a * b;
    }

    public static void main(String[] args) {
        System.out.println(Multiply.multiply(2.0, 5.0));
        System.out.println(Multiply.multiply(3.0, 3.0));
        System.out.println(Multiply.multiply(3.333, 12.123));
    }
}
